package com.learnerscloud.gcsebiologytutorvideosamazon;

public class VideoItem  extends Object {
	
	private String Title;
	private String Description;
	private String Free;
	private String M3u8;
	private int Subscribeimage;
	private int Searchimage;
	
	
	public int getSearchimage() {
		return Searchimage;
	}
	public void setSearchimage(int searchimage) {
		Searchimage = searchimage;
	}
	public int getSubscribeimage() {
		return Subscribeimage;
	}
	public void setSubscribeimage(int subscribeimage) {
		Subscribeimage = subscribeimage;
	}
	public String getTitle() {
		return Title;
	}
	public void setTitle(String title) {
		Title = title;
	}
	public String getDescription() {
		return Description;
	}
	public void setDescription(String description) {
		Description = description;
	}
	public String getFree() {
		return Free;
	}
	public void setFree(String free) {
		Free = free;
	}
	public String getM3u8() {
		return M3u8;
	}
	public void setM3u8(String m3u8) {
		M3u8 = m3u8;
	}
	
	
	

}
